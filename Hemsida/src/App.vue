<template>
  <v-app id="content">
    <v-toolbar app id="toolbar">
      <v-toolbar-title class="headline text-uppercase">
        <span>Robot</span>
        <span class="font-weight-light">Car</span>
      </v-toolbar-title>

      <v-spacer></v-spacer>      
       <v-btn color="primary" dark @click="openDialog">Anslutningsalternativ</v-btn>
      <v-btn target="_blank" @click="Switch=!Switch">
        <span v-if="Switch!==true" class="mr-2">Buttons 1</span>
        <span v-else class="mr-2">Buttons 2</span>
      </v-btn>
    </v-toolbar>

    <v-content>
      <v-row align="center" justify="center">
        <connectionsettings v-if="showDialog" />           
        <div>
          <Logger />

          <Buttons v-if="Switch" />
          <Buttons2 v-else />
        </div>
      </v-row>
    </v-content>
  </v-app>
</template>

<script>
// importerar de olika komponenterna
import Buttons2 from "./components/Buttonsv2";
import Buttons from "./components/Buttons";
import Logger from "./components/Logger";
import connectionsettings from "./components/connectionsettings";

// syntax som man skriver fÃ¶r att bygga upp logik kring komponenterna
export default {
  name: "App",
  components: {
    Buttons,
    Buttons2,
    Logger,
    connectionsettings
  },

  // de egenskaper som finns fÃ¶r komponenten
  data() {
    return {
      Switch: false,
      dialog: false,
      name: "",
      password: "",
      port: "",
      adress: ""
    };
  },
  // nÃ¤r man skapar upp komponenten sÃ¥ kÃ¶rs created funktionen
  created() {
    let User = this.$store.getters.GetUser;
    this.name = User.name;
    this.password = User.password;
    this.port = User.port;
    this.adress = User.adress;   
  },

// Ã¤ndra annslutnings konfiguration
  methods: {   
    openDialog(){      
        this.dialog = true;
        this.$store.dispatch("Dialog", this.dialog);
    }
  },
  computed: { 
    showDialog() {
      this.dialog = this.$store.getters.dialog;   
      return this.dialog;
    }
  }
};
</script>

